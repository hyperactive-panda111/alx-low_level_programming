#include "main.h"

/**
 * print_sign - prints the sign of the input integer
 *
 * @n: testing variable;
 *
 * Return: 1 if positive, 0 if zero or -1 if negative
 */

int print_sign(int n)
{
	if (n == 0)
	{
		_putchar(0);
		return (0);
	}
	else if (n > 0)
	{
		_putchar(1);
		return (1);
	}
	else if (n < 0)
	{
		_putchar(-1);
		return (-1);
	}
}
